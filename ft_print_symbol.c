#include "ft_printf.h"

int		ft_print_symbol(char c, int len)
{
	int	i;

	i = 0;
	if (len <= 0)
		return (0);
	while (i < len)
	{
		write(1, &c, 1);
		i++;
	}
	return (len);
}
